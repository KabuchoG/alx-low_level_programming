/**
 * abs : determines the absolute value of a number
 *
 * Return: returns a value int
 */
int _abs(int absolutval)
{
	int n = 0;
	
	absolutval = 0;
	if (n > 0)
	{
	absolutval = (0 + n);
	}
	else if (n < 0)
	{
	absolutval = (0 - n);
	}
	else
	{
	absolutval = 0;
	}
	return (0);
}
